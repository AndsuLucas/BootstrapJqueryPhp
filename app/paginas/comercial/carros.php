							<h2 class="featurette-heading" id="corolla ">Corolla</h2>
							<p class="lead" >A linha 2019 do Toyota Corolla foi anunciada em abril de 2018. O modelo recebeu pequenas novidades e ainda alterações nos preços. Na ocasião, o Corolla 2019 foi anunciado com preços entre R$ 89.990 e R$ 118.850. Porém, de lá para cá ele já recebeu reajustes e agora pode ser encontrado na rede de concessionárias com preços entre R$ 90.990 e R$ 118.990.</p>
							<p class="lead">Uma das principais novidades foi o reposicionamento do Toyota Corolla GLi Couro 1.8 CVT, que se posiciona como a opção mais em conta da gama e também o único com motor 1.8 litro flex. Ele recebeu uma redução de preço de quase R$ 3 mil frente ao antigo preço de R$ 92.690.</p>
							<p class="lead">Com isso, o Toyota Corolla 2019 passa a dispor de três opções de acabamento. O modelo de entrada GLi Couro sai equipado com bancos revestidos em couro cinza. Já o XEi e o XRS contam com interior e bancos totalmente em preto, também com revestimento em couro. Por fim, o topo de linha Altis dispõe de acabamento em couro na cor bege.</p>
							<p class="lead">Essas mudanças afirmam o ditado de “em time que está ganhando não se mexe”. O Corolla 2019 é o atual automóvel mais vendido na categoria de sedãs médios e supera inclusive até modelos mais baratos no ranking geral entre os carros de passeio.</p>
							<p class="lead">Uma nova geração do Toyota Corolla já foi apresentada na Europa e nos Estados Unidos com carroceria hatch, conhecida como Toyota Auris. Ele recebeu uma série de novidades, como a plataforma modular TNGA (a mesma do Prius), visual mais agressivo, novos equipamentos e motores mais eficientes.</p>
							<p class="lead">A versão sedã do novo Corolla já foi vista rodando em testes e deve ser revelada em meados de 2019. Portanto, é provável que o novo modelo demore um pouco para chegar ao Brasil, provavelmente apenas em 2020.</p>
							<p class="lead">As novidades do Corolla 2019 incluem retoques no visual e no acabamento interno, além de novos equipamentos de série desde a versão mais básica. O Novo Toyota Corolla 2019 chega a mercado nacional com preços entre R$ 69.690 e R$ 114.990, contando agora com o tão aguardado ESP e também uma nova versão com detalhes esportivos, chamada XRS.</p>
