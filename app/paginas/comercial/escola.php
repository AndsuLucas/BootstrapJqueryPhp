						<h2 class="featurette-heading" id="adm">Administração intregadas ao ensino médio.</h2>
						<p class="lead" >O administrador gerencia recursos financeiros, materiais ou humanos de uma empresa. Ele tem lugar em praticamente todos os departamentos de uma organização pública, privada ou sem fins lucrativos. É sua função definir estratégias e gerenciar o dia a dia da organização, traçando metas, criando políticas internas, realizando auditorias e analisando resultados.</p>
						<p class="lead">Em recursos humanos, responde pelo relacionamento da empresa com funcionários e sindicatos, gerencia admissões e demissões, planos de carreira e benefícios. Na área de compras, faz cotação de preços, providencia a aquisição de matérias-primas e insumos, negocia com fornecedores e controla os estoques.</p>

						<p class="lead">Em vendas, encarrega-se da negociação de preços, condições de pagamento e prazos de entrega com clientes e das atividades de publicidade e marketing. No departamento financeiro, lida com pagamentos e recebimentos, a agenda de impostos ou a cobrança de clientes. Em indústrias, trabalha no controle de qualidade e de estoques de produtos acabados.</p>

						<p class="lead">Ele pode, ainda, definir as políticas corporativas, preocupando-se, por exemplo, com ações de sustentabilidade e responsabilidade social. Seja qual for a área de atuação, este bacharel precisa se manter atualizado com a economia mundial e nacional, as tendências de consumo e a legislação na área de atuação da empresa. Cursos de especialização, como MBAs, são essenciais para crescer na carreira. Você pode ingressar no mercado como tecnólogo. </p>
