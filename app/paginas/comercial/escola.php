						<h2 class="featurette-heading" id="informatica">Informatica intregadas ao ensino médio.</h2>
						<p class="lead" >O curso pode incluir mais de uma qualificação (generalista) ou ao contrário, optar por formar o especialista em uma área de atuação. Em Informática generalista, é um profissional que pode ter mais de uma atuação ou função, como Operador de Microcomputador, Auxiliar de Manutenção ou Programador em alguma Linguagem de Programação. Isto é possível porque dentro do Curso há módulos específicos para cada uma destas ocupações. Ao contrário, o técnico especialista tem a maior parte da carga horária do curso focada em uma sub-área. Podemos reconhecer pelo título do curso que geralmente se empresta: Técnico em Desenvolvimento de Sistemas, Técnico em Redes de Computadores, Técnico em Montagem e Manutenção de Equipamentos de Informática, etc.</p>

						<p class="lead">Sendo a profissão de técnico em informática reconhecida, mas não regulamentada, qualquer escola legalmente constituída pode oferecer a formação do Técnico em Informática, e o Certificado legalmente expedido e registrado em livro próprio tem validade Nacional. Garantido pelo artigo 5º da Constituição inciso XIII: "é livre o exercício de qualquer trabalho, ofício ou profissão, atendidas as qualificações que a lei estabelecer", e a ausência de uma lei como determina a CRB/88 em seu artigo 5º, inciso XIII c/c artigo 22, inciso XVI, não impede o exercício da profissão. Assim, não há quem possa gerar o impedimento do exercício profissional, haja vista que, o que a Constituição exige é a qualificação, ou seja, desde que o cidadão esteja devidamente qualificado poderá exercer a atividade profissional, sem qualquer restrição.</p>

						<p class="lead">O Certificado do curso de TI, é legalmente emitido ratifica a qualificação do técnico em informática. Este é o entendimento do STF quando afirma que “É inconstitucional atentar contra a liberdade consagrada na Constituição Federal, restringindo o exercício da profissão que não pressupõe condições de capacidade”</p>

						<p class="lead">Todo profissional de TI, para desenvolver em plena seu trabalho de cunho intelectual e intransigível, necessita de um local para repouso e pleno descanso corporal e mental, estando livre e afastado de qualquer atividade, é digno que este ambiente seja anexado ao seu ponto de desenvolvimento, cunho este inarredavelmente importante. A prática mostra que a análise dos diversos resultados cumpre um papel essencial na formulação das formas de ação. </p>
