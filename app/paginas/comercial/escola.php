						<h2 class="featurette-heading" id="etec">O que seria uma Etec?</h2>
						<p class="lead" >É um Ensino Técnico é um tipo de ensino profissionalizante. Ele possibilita que o aluno curse, além do Ensino Médio, um curso técnico e se forme na escola já com uma profissão. Isso faz com que ele saia na frente na corrida por uma vaga de emprego, antes mesmo de ingressar na faculdade.</p>

						<p class="lead">Tiago Sereza, gerente de integração da Catho Educação, explica: “Atualmente o mercado para esses profissionais é bastante promissor. Por um bom tempo, as pessoas acreditaram que uma posição melhor estava condicionada apenas a um curso superior, deixando uma lacuna nas empresas que precisam de profissionais com formação técnica”.</p>

						<p class="lead">“Apesar disso, o Ensino Técnico é cursado apenas por 5% dos jovens de 14 a 17 anos em São Paulo. No Chile, são 40%. Na Argentina e no Uruguai, 25%. Em Cuba, mais de 50%”, conta Almério Melquíades de Araújo, coordenador do Ensino Médio e Técnico do Centro Paula Souza, que administra 200 escolas técnicas, entre elas, a ETEC Getúlio Vargas, localizada em São Paulo.</p>

						<p class="lead">Entre as áreas que oferecem mais oportunidades para nível técnico, estão Tecnologia da Informação, Segurança do Trabalho, Petróleo e Gás e Construção Civil. Mas há outras áreas em expansão que são também bastante concorridas, como Técnico em Enfermagem, Técnico em Administração e Logística. </p>
